package com.homework_4.task_1_Geometry.Point;

public class Point_3D extends Vertics {
    private double z;

    public Point_3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}