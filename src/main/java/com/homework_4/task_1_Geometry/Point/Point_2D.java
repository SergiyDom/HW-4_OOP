package com.homework_4.task_1_Geometry.Point;

public class Point_2D extends Vertics {
    private double x;
    private double y;

    public Point_2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
